public class ImpliziteKonvertierung
{
   public static void main (String[] args)
   {
      System.out.println ("Division von 10 durch 12: "
                           + (10/12));
      System.out.println ("Division von 10. durch 12: "
                          + (10./12));
      System.out.println ("Division von 10 durch 12.: "
                           + (10/12.));
   }
}
